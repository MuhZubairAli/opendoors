<h3>Why Paid Data?</h3>
<p class="contents"> While this website provides an extensive amount of financial and share prices data of the companies listed at the Pakistan Stock Exchange, the data is in original form as obtained from the primary sources. That means that the data needs proper cleaning, matching, and organizing before it can be used in any statistical or financial of analysis.</p>

<p class="contents"> Realizing that some researchers might not have the luxury of sufficient time, we provide paid help in collecting, organizing, and cleaning various types of financial and non-financial data of companies. The following data can be arranged for the mentioned prices.</p>

<h4>There is more to it than just getting ready-made data?</h4>
<p class="contents"> The data we collect and share it through www.OpenDoors.pk requires a lot of efforts, time, and money in some cases as we pay for collecting and organizing hard to find data. To sustain all these efforts and to ensure the economic viability of this service, paid data service is a good way forward. Thus, your payment finances availability of other hard to find data. This means that paying for data not only enables you to get organized data but also serves as a contribution from your side toward the researchers' community.</p>

