$cjs = @"
$(function(){
    $('#sterm').on('input',function(){
        //console.log($(this).val());
        if($(this).val().trim() == ''){
            showAllItems();
        }
        else {
            showQueryItems($(this).val());
        }
    });
});

function showQueryItems(query){
        hideAllItems();
        for(i = 1; i < 68; i++){
            if($('#service-item-' + i).text().toLowerCase().includes(query.toLowerCase())){
                $('#service-item-' + i).show();
        }
    }
}

function hideAllItems(){

    for(i = 1; i < 68; i++){
        $('#service-item-' + i).hide();
    }
}

function showAllItems(){
    for(i = 1; i < 68; i++){
        $('#service-item-' + i).show();
    }
}
";
$this->plugin->addCustomJS($cjs);